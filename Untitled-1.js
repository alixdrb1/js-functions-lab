// write a function to take in a character, so that, 
// if this character is 'k' or 'x' then return True, else return False

const tempFunction = (letter) => {
    if (letter == 'k') {
        return "True";
    } else if (letter == 'x') {
        return "True";
    } else {
        return "False";
    }
}

let x = 'x';
console.log(tempFunction(x));
console.log(tempFunction('k'));


